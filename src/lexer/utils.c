#include "lexer_internal.h"

int	is_whitespace(char c)
{
	return ((c >= 9 && c<= 12) || (c == 32));
}

int	is_whitespace_or_special(char c)
{
	return (c == '<' || c == '>' || c == '|' || c == '&'
			|| c == ';' || (c >= 9 && c<= 12) || (c == 32));
}

int	is_special(char c)
{
	return (c == '<' || c == '>' || c == '|' || c == '&' || c == ';');
}